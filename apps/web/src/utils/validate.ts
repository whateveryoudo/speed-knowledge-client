export const rges = {
    email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
    name: /^[a-zA-Z0-9\u4e00-\u9fa5\s]{4,16}$/,
    // 用户名：3-50位，支持字母、数字、下划线、中文字符
    username: /^[a-zA-Z0-9_\u4e00-\u9fa5]{3,50}$/,
    // 密码：至少12位，包含数字、大小写字母和特殊符号
    password: /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[!@#$%^&*()_+\-=\[\]{};':"\\|,.<>\/?]).{10,}$/,
    nickname: /^[a-zA-Z0-9\u4e00-\u9fa5\s]{2,16}$/,
}